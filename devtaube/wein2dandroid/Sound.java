package devtaube.wein2dandroid;

public class Sound
{
    // Variables ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private int soundID = -1;
    private int streamID;
    protected static App app;
    // Constructor ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public Sound(int resource)
    {
        soundID = app.soundPool.load(app.context, resource, 0);
    }
    // Methods for controlling the Sound ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void play()
    {
        streamID = app.soundPool.play(soundID, 1, 1, 0, 0, 1);
    }
    public void stop()
    {
        app.soundPool.stop(streamID);
    }
}
